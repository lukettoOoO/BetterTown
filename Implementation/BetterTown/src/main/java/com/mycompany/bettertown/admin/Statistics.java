    private int count=0;
    private long totalSeconds=0;
    public Statistics() {
        initComponents();
        issues=DatabaseLogic.getAllIssues();
        solvedIssues=DatabaseLogic.getAllSolvedIssues();
        Number.setText(String.valueOf(issues.size()));
        for(IssueData i : issues)
        {
            if(i.getStatus().equals("Resolved"))
            {
                solved++;
            }
            if(i.getStatus().equals("In Progress"))
            {
                progress++;
            }
            if(i.getStatus().equals("Not resolved"))
            {
                unresolved++;
            }
        }
        Unresolved.setText(String.valueOf(unresolved));
        Progress.setText(String.valueOf(progress));
        Solved.setText(String.valueOf(solved));
        for(SolvedIssues s: solvedIssues)
        {
            for(IssueData i : issues)
            {
                if(i.getId()==s.getIssueId())
                {System.out.println(i.getTitle());
                 Duration d=Duration.between(i.getDate(), s.getDate());
                 totalSeconds+=d.getSeconds();
                 count++;
                 //break;
                }
            }
        }
        if(count > 0)
        {
            long mediaSecunde = totalSeconds / count;

            long zile = mediaSecunde / (24 * 60 * 60);
            long ore = (mediaSecunde % (24 * 60 * 60)) / 3600;
            long minute = (mediaSecunde % 3600) / 60;

            String average = zile + " zile, " + ore + " ore È™i " + minute + " minute";
            Average.setText(average);
        }
        else
        {
            Average.setText("");
        }
    }

